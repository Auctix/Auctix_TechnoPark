import UIKit

extension UIColor {
    public class var honeyYellow  : UIColor {
        return UIColor(named: "Honey Yellow")   ?? UIColor.white
    }
    public class var  prussianBlue : UIColor {
        return UIColor(named: "Prussian Blue")   ?? UIColor.white
    }
    public class var blueGreen  : UIColor {
        return UIColor(named: "Blue Green")   ?? UIColor.white
    }
    public class var lightCornflowerBlue  : UIColor {
        return UIColor(named: "Light Cornfiower Blue")   ?? UIColor.white
    }
    public class var searchColor : UIColor {
        return UIColor(named: "Search Blue") ?? UIColor.white
    }
    public class var lightGrad : UIColor {
        return UIColor(named: "Grad1")   ?? UIColor.white
    }
    public class var darkGrad : UIColor {
        return UIColor(named: "Grad2") ?? UIColor.white
    }
}

